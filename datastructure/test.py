# python 数据接口的使用
#python list集合 使用方式
list = [5,7,1,8,0,2,7,9,3,6]
print(list[0])

#python 数组 使用方式
array = (1,2,3,4,5,6)
print(array[1:5])

#python set集合 使用方式
set = {'Google', 'Taobao', 'Runoob', 'Facebook', 'Zhihu', 'Baidu','Taobao'}
print(set)

#python map 使用方式
tinydict = {'name': 'runoob','code':1, 'site': 'www.runoob.com'}
print(tinydict.keys())
print(tinydict.values())